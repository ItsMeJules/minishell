#include "minishell.h"

int	ft_pwd(t_list *env, t_list *vars)
{
	ft_putstr_fd(get_env_val(env, "PWD"), 1);
	ft_putchar_fd('\n', 1);
	mod_env(&vars, "?", "0");
	return (0);
}
